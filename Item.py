class Item:

	def setItemId(self, id):
		self.__id = id

	def setItemName(self, name):
		self.__name = name

	def setItemPrice(self, price):
		self.__price == price

	def setItemSize(self, size):
		self.__size = size

	def getItemId(self):
		return self.__id

	def getItemName(self):
		return self.__name

	def getItemPrice(self):
		return self.__price
		
	def getItemSize(self):
		return self.__size